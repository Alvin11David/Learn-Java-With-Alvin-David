public class Variables {
    public static void main(String[] args) {
        //String Data type - mainly deals with text
        String name = "Alvin David";
        System.out.println(name);

        //Integer - deals with whole numbers without decimals
        int age = 25;
        System.out.println(age);

        /*float - deals with numbers with decimals and it must end with f
        to tell the compiler that it is a float number
         */
        float salary = 20000.00f;
        System.out.println(salary);

        //Char - this deals with a single character such as an alphabet
        char letter = 'A';
        System.out.println(letter);

        //boolean - deals with whether something is true/false
        boolean isLearningJava = true;
        System.out.println(isLearningJava);
    }
}